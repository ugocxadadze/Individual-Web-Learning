#1)ახსენი რა არის len() , append() , remove() ფუნქციები და რისთვის გამოიყენება ისინი
#len()ფუნქციით შეგვიძლია გავიგოდ სტრინგის ან სიის სიგრძე
my_foods=["პიცა" , "გუფთა" , "სბაგეტი"]
len(my_foods)
print(len(my_foods))
#append ფუნქციით შეგვიძლია დავამატოთ სიაში ელემენტი
my_foods.append("მანდარინი")
print(my_foods)
#remove ფუნქცია შლის ელემენტს
my_foods.remove("გუფთა")
print(my_foods)